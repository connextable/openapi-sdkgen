func TestPackageArtifactsStayConsistentAndDeterministic(t *testing.T) {
	document, err := sdkgen.Compile([]byte(emitterFixture))
	if err != nil {
		t.Fatal(err)
	}
	first, err := PackageArtifacts(document, Package{Name: "@example/api", Directory: "sdk/example"})
	if err != nil {
		t.Fatal(err)
	}
	second, err := PackageArtifacts(document, Package{Name: "@example/api", Directory: "sdk/example"})
	if err != nil {
		t.Fatal(err)
	}
	if len(first) != len(second) {
		t.Fatalf("artifact count = %d, want %d", len(first), len(second))
	}
	artifacts := make(map[string][]byte, len(first))
	for index := range first {
		if first[index].Path != second[index].Path || !bytes.Equal(first[index].Data, second[index].Data) {
			t.Fatalf("artifact %d is not deterministic", index)
		}
		artifacts[first[index].Path] = first[index].Data
	}

	typesSource := string(artifacts["sdk/example/generated/types.ts"])
	for _, expected := range []string{
		"export type ProductInput =",
		"export type ProductOutput =",
		"readonly note?: string | undefined",
		`export type MixedLiteral = "ready" | 2 | null`,
		"export type CoordinateInput = readonly [number, number]",
		"export type CoordinateOutput = readonly [number, number]",
	} {
		if !strings.Contains(typesSource, expected) {
			t.Errorf("types missing %q\n%s", expected, typesSource)
		}
	}
	productInputStart := strings.Index(typesSource, "export type ProductInput =")
	productOutputStart := strings.Index(typesSource, "export type ProductOutput =")
	pageStart := strings.Index(typesSource, "export type ProductPageInput =")
	productInput := typesSource[productInputStart:productOutputStart]
	productOutput := typesSource[productOutputStart:pageStart]
	if strings.Contains(productInput, "readonly id:") || !strings.Contains(productInput, "readonly secret:") {
		t.Fatalf("input readOnly/writeOnly projection is wrong:\n%s", productInput)
	}
	if !strings.Contains(productOutput, "readonly id:") || strings.Contains(productOutput, "readonly secret:") {
		t.Fatalf("output readOnly/writeOnly projection is wrong:\n%s", productOutput)
	}
	if strings.Contains(typesSource, "HiddenOnly") {
		t.Fatalf("hidden-only component leaked:\n%s", typesSource)
	}
	for _, expected := range []string{
		"Catalog product values.",
		"Stable product identifier.",
		"Present in responses; omitted from generated input projections.",
		"Legacy product note.",
		"Format: `opaque-id`.",
		"@default \"legacy\"",
		"@deprecated This OpenAPI value is deprecated.",
		"Map of every public OpenAPI component name",
		"readonly id: Identifier",
	} {
		if !strings.Contains(typesSource, expected) {
			t.Fatalf("type JSDoc missing %q:\n%s", expected, typesSource)
		}
	}
	if strings.Contains(typesSource, "(Identifier) & (unknown)") {
		t.Fatalf("annotation-only $ref siblings changed the generated type:\n%s", typesSource)
	}

	clientSource := string(artifacts["sdk/example/generated/client.ts"])
	errorsSource := string(artifacts["sdk/example/generated/errors.ts"])
	runtimeSource := string(artifacts["sdk/example/generated/runtime.ts"])
	readme := string(artifacts["sdk/example/README.md"])
	if !strings.HasPrefix(runtimeSource, "// Code generated by sdkgen. DO NOT EDIT.\n\n") || !strings.Contains(runtimeSource, "export function createRequest") {
		t.Fatalf("generated runtime missing or invalid:\n%s", runtimeSource)
	}
	for _, expected := range []string{
		"Stable error codes for failures produced by the SDK transport layer.",
		"Configuration shared by every operation on a generated client.",
		"Successful response including decoded data",
		"Creates a lazy async iterator over all items",
		"Creates the endpoint-neutral Fetch API request executor",
		"@param options Client-wide base URL and transport defaults.",
	} {
		if !strings.Contains(runtimeSource, expected) {
			t.Fatalf("runtime JSDoc missing %q:\n%s", expected, runtimeSource)
		}
	}
	if strings.Contains(clientSource, "../../runtime") || !strings.Contains(clientSource, `from "./runtime.js"`) {
		t.Fatalf("client does not use its generated package runtime:\n%s", clientSource)
	}
	if strings.Contains(errorsSource, "../../runtime") || !strings.Contains(errorsSource, `from "./runtime.js"`) {
		t.Fatalf("errors do not use the generated package runtime:\n%s", errorsSource)
	}
	if strings.Contains(errorsSource, "Contract.") != strings.Contains(errorsSource, `import type * as Contract from "./types.js"`) {
		t.Fatalf("errors Contract import does not match generated references:\n%s", errorsSource)
	}
	for _, surface := range []string{clientSource, readme} {
		if !strings.Contains(surface, "createProduct") {
			t.Errorf("visible operation missing:\n%s", surface)
		}
		if strings.Contains(surface, "hiddenAudit") {
			t.Errorf("hidden operation leaked:\n%s", surface)
		}
	}
	if strings.Contains(clientSource, "@operationID") || strings.Contains(clientSource, "@http") {
		t.Fatalf("invalid operation JSDoc tags emitted:\n%s", clientSource)
	}
	for _, expected := range []string{
		"Output of `createProduct` (`POST /products`).",
		"Schema: {@link Contract.ProductOutput}.",
		"Operation ID: `createProduct`.",
		"HTTP: `POST /products`.",
		"* ```ts\n * await api.products.create({ body }, { idempotencyKey })\n * ```",
		"Sends the request and returns the decoded response body.",
		"@returns {@link CreateProductOutput}",
		"Sends the request and returns the decoded body with HTTP response metadata.",
		"@returns {@link CreateProductRawResponse}",
		"Creates one catalog product from the supplied body.",
		"Product values accepted during creation.",
		"Product identifier.",
		"Opaque continuation cursor.",
		"Case-insensitive product search.",
		"Sort expression for `listProducts`.",
		"Ordered sort expressions applied by the server.",
		"Status- and media-aware raw response for `createProduct`",
		"`201 application/json` — Created",
		"Header `X-Request-Id` — Request correlation identifier.",
		"Creates a generated API client.",
		"Type catalog keyed by OpenAPI operation ID.",
		"Lazily iterates every item from {@link Operations.listProducts} pagination.",
		"@deprecated This operation is deprecated.",
	} {
		if !strings.Contains(clientSource, expected) {
			t.Fatalf("operation JSDoc missing %q:\n%s", expected, clientSource)
		}
	}
	if !strings.Contains(clientSource, "POST /products") {
		t.Fatalf("operation JSDoc missing:\n%s", clientSource)
	}
	if !strings.Contains(clientSource, "readonly paginate:") || !strings.Contains(clientSource, "AsyncIterable<Contract.ProductOutput>") || !strings.Contains(clientSource, `createPaginator<Contract.ProductOutput`) {
		t.Fatalf("pagination helper missing:\n%s", clientSource)
	}
	if !strings.Contains(clientSource, `export type CreateProductOptions = Omit<RequestOptions, "accept" | "idempotencyKey" | "ifMatch"> & {`) || !strings.Contains(clientSource, "Required idempotency key") || !strings.Contains(clientSource, "readonly idempotencyKey: string") {
		t.Fatalf("required idempotency option missing:\n%s", clientSource)
	}
	if !strings.Contains(clientSource, "readonly createProduct: CreateProductCall") || !strings.Contains(clientSource, "(input: CreateProductInput, options: CreateProductOptions): Promise<CreateProductOutput>") {
		t.Fatalf("raw-capable operation call missing:\n%s", clientSource)
	}
	if !strings.Contains(clientSource, "(productID: string): {") || !strings.Contains(clientSource, "bindPathOperation<GetProductInput, GetProductResourceInput") {
		t.Fatalf("instance resource builder missing:\n%s", clientSource)
	}
	for _, expected := range []string{
		`export type ServerErrorCode = "validation_failed"`,
		`export type ValidationFailedError = APIError<"validation_failed">`,
		"export function isValidationFailedError",
		"export function isValidationError",
		"export type CreateProductServerError = ValidationFailedError",
		"export type CreateProductError = CreateProductServerError | TransportError",
		"The request failed validation.",
		"Checks whether an unknown value is a {@link ValidationFailedError}.",
		"Server errors in the `validation` category.",
		"Server-declared errors for `createProduct` (`POST /products`).",
	} {
		if !strings.Contains(errorsSource, expected) {
			t.Errorf("errors missing %q\n%s", expected, errorsSource)
		}
	}

	var manifest Manifest
	if err := json.Unmarshal(artifacts["sdk/example/manifest.json"], &manifest); err != nil {
		t.Fatal(err)
	}
	if len(manifest.Operations) != 4 || manifest.Operations[0].OperationID != "hiddenAudit" || manifest.Operations[1].OperationID != "listProducts" || manifest.Operations[2].OperationID != "createProduct" || manifest.Operations[3].OperationID != "getProduct" {
		t.Fatalf("manifest operations = %#v", manifest.Operations)
	}
	if manifest.Operations[2].CallExpression != "api.products.create({ body }, { idempotencyKey })" || manifest.Operations[2].OutputType != "ProductOutput" {
		t.Fatalf("create manifest = %#v", manifest.Operations[2])
	}
	if manifest.FormatVersion != 2 || manifest.Operations[2].Description != "Creates one catalog product from the supplied body." {
		t.Fatalf("manifest JSDoc metadata = %#v", manifest.Operations[2])
	}
	if manifest.Operations[3].CallExpression != "api.products(productID).get()" {
		t.Fatalf("get manifest = %#v", manifest.Operations[3])
	}
	if !manifest.Operations[3].Deprecated {
		t.Fatalf("deprecated operation metadata missing: %#v", manifest.Operations[3])
	}
	assertGeneratedJSDocCoverage(t, generatedJSDocCoverage{name: "types", source: typesSource, nestedReadonly: true})
	assertGeneratedJSDocCoverage(t, generatedJSDocCoverage{name: "client", source: clientSource, nestedReadonly: true})
	assertGeneratedJSDocCoverage(t, generatedJSDocCoverage{name: "errors", source: errorsSource, nestedReadonly: true})
	assertGeneratedJSDocCoverage(t, generatedJSDocCoverage{name: "runtime", source: runtimeSource})
}

func TestPackageArtifactsGenerateNestedResourceTree(t *testing.T) {
	document, err := sdkgen.Compile([]byte(nestedResourceFixture))
	if err != nil {
		t.Fatal(err)
	}
	artifacts, err := PackageArtifacts(document, Package{Name: "@example/api", Directory: "sdk/example"})
	if err != nil {
		t.Fatal(err)
	}
	byPath := make(map[string][]byte, len(artifacts))
	for _, artifact := range artifacts {
		byPath[artifact.Path] = artifact.Data
	}
	client := string(byPath["sdk/example/generated/client.ts"])
	for _, expected := range []string{
		"readonly auth: {",
		"readonly login: {",
		"readonly post: LoginCall",
		"readonly sessions: {",
		"(sessionID: string): {",
		"readonly delete: RevokeSessionResourceCall",
		"login: {\n      post: login,",
		"sessions: Object.assign(",
	} {
		if !strings.Contains(client, expected) {
			t.Fatalf("nested resource surface missing %q:\n%s", expected, client)
		}
	}
	var manifest Manifest
	if err := json.Unmarshal(byPath["sdk/example/manifest.json"], &manifest); err != nil {
		t.Fatal(err)
	}
	got := make(map[string]string, len(manifest.Operations))
	for _, operation := range manifest.Operations {
		got[operation.OperationID] = operation.CallExpression
	}
	for id, want := range map[string]string{
		"login":          "api.auth.login.post()",
		"revokeSessions": "api.auth.sessions.delete()",
		"revokeSession":  "api.auth.sessions(sessionID).delete()",
		"requestCode":    "api.auth.signup.verificationCode.post()",
	} {
		if got[id] != want {
			t.Fatalf("%s call = %q, want %q", id, got[id], want)
		}
	}
}

type generatedJSDocCoverage struct {
	name           string
	source         string
	nestedReadonly bool
}

func assertGeneratedJSDocCoverage(t *testing.T, coverage generatedJSDocCoverage) {
	t.Helper()
	lines := strings.Split(coverage.source, "\n")
	for index, line := range lines {
		trimmed := strings.TrimSpace(line)
		isExport := strings.HasPrefix(trimmed, "export ") && trimmed != "export {" && !strings.HasPrefix(trimmed, "export type {")
		isNestedReadonly := coverage.nestedReadonly && strings.HasPrefix(trimmed, "readonly ")
		if !isExport && !isNestedReadonly {
			continue
		}
		previous := index - 1
		for previous >= 0 && strings.TrimSpace(lines[previous]) == "" {
			previous--
		}
		if previous < 0 || !strings.HasSuffix(strings.TrimSpace(lines[previous]), "*/") {
			t.Errorf("%s declaration lacks JSDoc at line %d: %s", coverage.name, index+1, trimmed)
		}
	}
}

