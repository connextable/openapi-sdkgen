func TestSourceArtifactsGenerateNestedResourceTree(t *testing.T) {
	document, err := sdkgen.Compile([]byte(nestedResourceFixture))
	if err != nil {
		t.Fatal(err)
	}
	artifacts, err := SourceArtifacts(document)
	if err != nil {
		t.Fatal(err)
	}
	byPath := make(map[string][]byte, len(artifacts))
	for _, artifact := range artifacts {
		byPath[artifact.Path] = artifact.Data
	}
	client := string(byPath["generated/client.ts"])
	for _, expected := range []string{
		"readonly auth: {",
		"readonly login: {",
		"readonly post: LoginCall",
		"readonly sessions: {",
		"(sessionID: string): {",
		"readonly delete: RevokeSessionResourceCall",
		"login: {\n      post: login,",
		"sessions: Object.assign(",
	} {
		if !strings.Contains(client, expected) {
			t.Fatalf("nested resource surface missing %q:\n%s", expected, client)
		}
	}
	manifest, err := buildManifest(document)
	if err != nil {
		t.Fatal(err)
	}
	got := make(map[string]string, len(manifest.Operations))
	for _, operation := range manifest.Operations {
		got[operation.OperationID] = operation.CallExpression
	}
	for id, want := range map[string]string{
		"login":          "api.auth.login.post()",
		"revokeSessions": "api.auth.sessions.delete()",
		"revokeSession":  "api.auth.sessions(sessionID).delete()",
		"requestCode":    "api.auth.signup.verificationCode.post()",
	} {
		if got[id] != want {
			t.Fatalf("%s call = %q, want %q", id, got[id], want)
		}
	}
}

type generatedJSDocCoverage struct {
	name           string
	source         string
	nestedReadonly bool
}

func assertGeneratedJSDocCoverage(t *testing.T, coverage generatedJSDocCoverage) {
	t.Helper()
	lines := strings.Split(coverage.source, "\n")
	for index, line := range lines {
		trimmed := strings.TrimSpace(line)
		isExport := strings.HasPrefix(trimmed, "export ") && trimmed != "export {" && !strings.HasPrefix(trimmed, "export type {")
		isNestedReadonly := coverage.nestedReadonly && strings.HasPrefix(trimmed, "readonly ")
		if !isExport && !isNestedReadonly {
			continue
		}
		previous := index - 1
		for previous >= 0 && strings.TrimSpace(lines[previous]) == "" {
			previous--
		}
		if previous < 0 || !strings.HasSuffix(strings.TrimSpace(lines[previous]), "*/") {
			t.Errorf("%s declaration lacks JSDoc at line %d: %s", coverage.name, index+1, trimmed)
		}
	}
}

