//go:embed runtime.ts
var runtimeTemplate []byte

// Artifact is a generated TypeScript source file.
type Artifact = generator.Artifact

// Generator emits TypeScript SDK source files from compiler IR.
type Generator struct{}

// Name returns the CLI target name.
func (Generator) Name() string { return "typescript" }

// SupportsAddon reports artifact sets available for TypeScript source output.
func (Generator) SupportsAddon(addon generator.Addon) bool {
	return addon == generator.AddonServer
}

// Generate emits source files relative to the selected output directory.
func (Generator) Generate(document *ir.Document, options generator.Options) ([]generator.Artifact, error) {
	return sourceArtifacts(document, options.HasAddon(generator.AddonServer))
}

type Manifest struct {
	Operations []ManifestOperation `json:"operations"`
}

type ManifestOperation struct {
	OperationID        string   `json:"operationID"`
	Summary            string   `json:"summary,omitempty"`
	Description        string   `json:"description,omitempty"`
	Method             string   `json:"method"`
	Path               string   `json:"path"`
	CallExpression     string   `json:"callExpression"`
	ResourceSegments   []string `json:"resourceSegments"`
	PathParameterOrder []string `json:"pathParameterOrder"`
	InputTypes         []string `json:"inputTypes"`
	OutputType         string   `json:"outputType"`
	ErrorType          string   `json:"errorType"`
	Envelope           string   `json:"envelope,omitempty"`
	Concurrency        string   `json:"concurrency,omitempty"`
	Idempotency        string   `json:"idempotency,omitempty"`
	Pagination         string   `json:"pagination,omitempty"`
	Auth               string   `json:"auth"`
	Visibility         string   `json:"visibility"`
	Deprecated         bool     `json:"deprecated"`
}

const generatedFileHeader = `// <auto-generated />
// @generated by openapi-sdkgen
// Code generated by openapi-sdkgen. DO NOT EDIT.
// This file is generated. Do not edit, lint, or format it manually.
// @ts-nocheck
/* eslint-disable */
/* oxlint-disable */
/* biome-ignore-all lint: generated file */
/* deno-lint-ignore-file */
// dprint-ignore-file
// prettier-ignore
// cspell:ignore

`

// SourceArtifacts emits TypeScript source that a consumer compiles as part of
// its own application. It deliberately contains no package metadata or build
// configuration.
func SourceArtifacts(document *ir.Document) ([]Artifact, error) {
	return sourceArtifacts(document, false)
}

func sourceArtifacts(document *ir.Document, includeServer bool) ([]Artifact, error) {
	if document == nil {
		return nil, fmt.Errorf("IR document is nil")
	}
	if err := validateSchemaSupport(document); err != nil {
		return nil, err
	}
	if includeServer {
		if err := validateServerInboundSchemaSupport(document); err != nil {
			return nil, err
		}
	}
	if err := validateOpenAPISupportWithServer(document, "TypeScript", includeServer); err != nil {
		return nil, err
	}
	if err := validateOperationSymbols(document); err != nil {
		return nil, err
	}
	manifest, err := buildManifest(document)
	if err != nil {
		return nil, err
	}
	typesSource, err := emitTypes(document)
	if err != nil {
		return nil, err
	}
	errorsSource, err := emitErrors(document)
	if err != nil {
		return nil, err
	}
	metadataSource, err := emitMetadata(document, true)
	if err != nil {
		return nil, err
	}
	clientSource, err := emitClient(document, manifest)
	if err != nil {
		return nil, err
	}
	if err := validateSourceExportSymbols(map[string][]byte{
		"types":    typesSource,
		"errors":   errorsSource,
		"metadata": metadataSource,
		"client":   clientSource,
	}); err != nil {
		return nil, err
	}
	indexSource := []byte("export * from \"./types.js\"\nexport * from \"./errors.js\"\nexport * from \"./client.js\"\n")

	artifacts := []Artifact{
		{Path: "index.ts", Data: generatedSource([]byte("export * from \"./generated/index.js\"\n"))},
		{Path: "generated/types.ts", Data: generatedSource(typesSource)},
		{Path: "generated/client.ts", Data: generatedSource(clientSource)},
		{Path: "generated/errors.ts", Data: generatedSource(errorsSource)},
		{Path: "generated/index.ts", Data: generatedSource(indexSource)},
		{Path: "metadata.ts", Data: generatedSource(metadataSource)},
		{Path: "generated/runtime.ts", Data: generatedSource(runtimeTemplate)},
	}
	if includeServer {
		serverArtifacts, err := emitServerArtifacts(document)
		if err != nil {
			return nil, err
		}
		artifacts = append(artifacts, serverArtifacts...)
	}
	sort.Slice(artifacts, func(i, j int) bool { return artifacts[i].Path < artifacts[j].Path })
	return artifacts, nil
}

func generatedSource(source []byte) []byte {
	return append([]byte(generatedFileHeader), source...)
}

func validateSourceExportSymbols(modules map[string][]byte) error {
	owners := make(map[string]string)
	moduleNames := make([]string, 0, len(modules))
	for module := range modules {
		moduleNames = append(moduleNames, module)
	}
	sort.Strings(moduleNames)
	for _, module := range moduleNames {
		exports := exportedSymbols(string(modules[module]))
		for symbol := range exports {
			if previous, exists := owners[symbol]; exists && previous != module {
				return fmt.Errorf("generated source export %q is declared by both %s and %s modules", symbol, previous, module)
			}
			owners[symbol] = module
		}
	}
	return nil
}

func exportedSymbols(source string) map[string]bool {
	result := make(map[string]bool)
	inExportBlock := false
	for _, line := range strings.Split(source, "\n") {
		trimmed := strings.TrimSpace(line)
		if inExportBlock {
			if trimmed == "}" {
				inExportBlock = false
				continue
			}
			name := strings.TrimSuffix(trimmed, ",")
			if before, after, found := strings.Cut(name, " as "); found {
				_ = before
				name = after
			}
			if name != "" {
				result[name] = true
			}
			continue
		}
		if !strings.HasPrefix(trimmed, "export ") {
			continue
		}
		rest := strings.TrimSpace(strings.TrimPrefix(trimmed, "export "))
		if rest == "{" || rest == "type {" {
			inExportBlock = true
			continue
		}
		if strings.HasPrefix(rest, "{") || strings.HasPrefix(rest, "type {") {
			open := strings.Index(rest, "{")
			close := strings.Index(rest, "}")
			if open >= 0 && close > open {
				for _, name := range strings.Split(rest[open+1:close], ",") {
					name = strings.TrimSpace(name)
					if before, after, found := strings.Cut(name, " as "); found {
						_ = before
						name = after
					}
					if name != "" {
						result[name] = true
					}
				}
			}
			continue
		}
		fields := strings.Fields(rest)
		if len(fields) < 2 {
			continue
		}
		switch fields[0] {
		case "type", "interface", "const", "function", "class":
			result[strings.Split(fields[1], "<")[0]] = true
		}
	}
	return result
}

func validateOperationSymbols(document *ir.Document) error {
	aliases := make(map[string]string)
	for _, operation := range document.Operations {
		if operation.Visibility == "hidden" {
			continue
		}
		typeName := operationTypeName(operation.OperationID)
		if previous, exists := aliases["type\x00"+typeName]; exists {
			return fmt.Errorf("operations %q and %q both generate TypeScript type name %q", previous, operation.OperationID, typeName)
		}
		aliases["type\x00"+typeName] = operation.OperationID
		property, err := naming.Property(operation.OperationID)
		if err != nil {
			return fmt.Errorf("operation %s: %w", operation.OperationID, err)
		}
		if previous, exists := aliases["property\x00"+property]; exists {
			return fmt.Errorf("operations %q and %q both generate TypeScript property %q", previous, operation.OperationID, property)
		}
		aliases["property\x00"+property] = operation.OperationID
	}
	return nil
}

func buildManifest(document *ir.Document) (Manifest, error) {
	manifest := Manifest{
		Operations: make([]ManifestOperation, 0, len(document.Operations)),
	}
	for _, operation := range document.Operations {
		outputType, err := operationOutputType(document, operation)
		if err != nil {
			return Manifest{}, fmt.Errorf("operation %s output: %w", operation.OperationID, err)
		}
		inputTypes, err := operationInputTypes(document, operation)
		if err != nil {
			return Manifest{}, fmt.Errorf("operation %s input: %w", operation.OperationID, err)
		}
		callExpression, segments, err := operationCall(operation, inputTypes)
		if err != nil {
			return Manifest{}, fmt.Errorf("operation %s call expression: %w", operation.OperationID, err)
		}
		visibility := operation.Visibility
		if visibility == "" {
			visibility = "public"
		}
		manifest.Operations = append(manifest.Operations, ManifestOperation{
			OperationID:        operation.OperationID,
			Summary:            operation.Summary,
			Description:        operation.Description,
			Method:             operation.Method,
			Path:               operation.Path,
			CallExpression:     callExpression,
			ResourceSegments:   segments,
			PathParameterOrder: append([]string{}, operation.PathParameterOrder...),
			InputTypes:         append([]string{}, inputTypes...),
			OutputType:         outputType,
			ErrorType:          operationTypeName(operation.OperationID) + "Error",
			Envelope:           operation.Envelope,
			Concurrency:        operation.Concurrency,
			Idempotency:        operation.Idempotency,
			Pagination:         operation.Pagination,
			Auth:               operationAuth(operation),
			Visibility:         visibility,
			Deprecated:         boolValue(operation.Raw, "deprecated"),
		})
	}
	return manifest, nil
}

func operationCall(operation ir.Operation, inputTypes []string) (string, []string, error) {
	parts := strings.Split(strings.Trim(operation.Path, "/"), "/")
	segments := make([]string, 0, len(parts))
	chain := "api"
	for _, part := range parts {
		if strings.HasPrefix(part, "{") && strings.HasSuffix(part, "}") {
			parameter, err := naming.Property(strings.TrimSuffix(strings.TrimPrefix(part, "{"), "}"))
			if err != nil {
				return "", nil, err
			}
			chain += "(" + parameter + ")"
			continue
		}
		property, err := naming.Property(part)
		if err != nil {
			return "", nil, err
		}
		segments = append(segments, property)
		chain += "." + property
	}
	terminal, err := resourceTerminalName(operation, parts)
	if err != nil {
		return "", nil, err
	}
	if operation.Visibility == "internal" {
		operationName, err := naming.Property(operation.OperationID)
		if err != nil {
			return "", nil, err
		}
		return "api.$operations." + operationName + callInput(operation, inputTypes, false, operation.PathParameterOrder), segments, nil
	}
	return chain + "." + terminal + callInput(operation, inputTypes, len(operation.PathParameterOrder) > 0, operation.PathParameterOrder), segments, nil
}

// resourceTerminalName keeps literal path segments as namespaces. For example,
// POST /auth/login becomes api.auth.login.post(), rather than api.auth.login().
func resourceTerminalName(operation ir.Operation, parts []string) (string, error) {
	terminal, err := terminalName(operation, parts)
	if err != nil {
		return "", err
	}
	if len(parts) == 0 {
		return terminal, nil
	}
	last := parts[len(parts)-1]
	if strings.HasPrefix(last, "{") {
		return terminal, nil
	}
	property, err := naming.Property(last)
	if err != nil {
		return "", err
	}
	if terminal != property {
		return terminal, nil
	}
	return methodTerminal(operation.Method)
}

func methodTerminal(method string) (string, error) {
	switch method {
	case "GET":
		return "get", nil
	case "POST":
		return "post", nil
	case "PUT":
		return "replace", nil
	case "PATCH":
		return "patch", nil
	case "DELETE":
		return "delete", nil
	case "QUERY":
		return "query", nil
	default:
		return naming.Property(strings.ToLower(method))
	}
}

func callInput(operation ir.Operation, inputTypes []string, pathBound bool, pathParameters []string) string {
	var fields []string
	for _, inputType := range inputTypes {
		switch {
		case strings.HasSuffix(inputType, "PathInput"):
			if pathBound {
				continue
			}
			values := make([]string, 0, len(pathParameters))
			for _, parameter := range pathParameters {
				property, err := naming.Property(parameter)
				if err == nil {
					values = append(values, property)
				}
			}
			fields = append(fields, "path: { "+strings.Join(values, ", ")+" }")
		case strings.HasSuffix(inputType, "QueryInput"):
			fields = append(fields, "query")
		case strings.HasSuffix(inputType, "HeaderInput"):
			fields = append(fields, "headerParams")
		case strings.HasSuffix(inputType, "CookieInput"):
			fields = append(fields, "cookieParams")
		case strings.HasSuffix(inputType, "BodyInput"):
			fields = append(fields, "body")
		}
	}
	var arguments []string
	if len(fields) > 0 {
		arguments = append(arguments, "{ "+strings.Join(fields, ", ")+" }")
	}
	var optionFields []string
	if operation.Idempotency == "required" {
		optionFields = append(optionFields, "idempotencyKey")
	}
	if operation.Concurrency == "required" {
		optionFields = append(optionFields, "ifMatch")
	}
	if len(optionFields) > 0 {
		arguments = append(arguments, "{ "+strings.Join(optionFields, ", ")+" }")
	}
	return "(" + strings.Join(arguments, ", ") + ")"
}

func terminalName(operation ir.Operation, parts []string) (string, error) {
	last := ""
	if len(parts) > 0 {
		last = parts[len(parts)-1]
	}
	if isTerminalAction(operation, parts, len(parts)-1) {
		return naming.Property(last)
	}
	lowerID := strings.ToLower(operation.OperationID)
	switch operation.Method {
	case "GET":
		if strings.HasPrefix(lowerID, "list") || strings.HasPrefix(lowerID, "search") {
			return "list", nil
		}
		return "get", nil
	case "POST":
		if strings.HasPrefix(lowerID, "create") {
			return "create", nil
		}
		return "post", nil
	case "PUT":
		return "replace", nil
	case "PATCH":
		return "patch", nil
	case "DELETE":
		return "delete", nil
	case "QUERY":
		return "query", nil
	default:
		return naming.Property(strings.ToLower(operation.Method))
	}
}

func isTerminalAction(operation ir.Operation, parts []string, index int) bool {
	if index < 0 || index != len(parts)-1 || operation.Method == "GET" || len(parts) < 2 {
		return false
	}
	last := parts[index]
	if last == "" || strings.HasPrefix(last, "{") {
		return false
	}
	return strings.Contains(operation.OperationID, strings.ToUpper(last[:1])+last[1:]) || !strings.HasPrefix(strings.ToLower(operation.OperationID), "create")
}

func operationTypeName(operationID string) string {
	name, err := naming.Public(operationID)
	if err != nil {
		return "Operation"
	}
	return name
}

func operationAuth(operation ir.Operation) string {
	security, exists := operation.Raw["security"]
	if !exists {
		return "inherited"
	}
	items, _ := security.([]any)
	if len(items) == 0 {
		return "public"
	}
	return "required"
}

func boolValue(values map[string]any, key string) bool {
	value, _ := values[key].(bool)
	return value
}
